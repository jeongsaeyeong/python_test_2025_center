# 섭씨온도를 화씨온도로 변환
# 공식: 화씨온도 = (9/5) x 섭씨온도 + 32

cel1 = 0
cel2 = 10
cel3 = 20
cel4 = 30
cel5 = 40
cel6 = 50

fah1 = (9/5) * cel1 + 32
fah2 = (9/5) * cel2 + 32
fah3 = (9/5) * cel3 + 32
fah4 = (9/5) * cel4 + 32
fah5 = (9/5) * cel5 + 32
fah6 = (9/5) * cel6 + 32

print("섭씨 화씨")
print(cel1, fah1)
print(cel2, fah2)
print(cel3, fah3)
print(cel4, fah4)
print(cel5, fah5)
print(cel6, fah6)